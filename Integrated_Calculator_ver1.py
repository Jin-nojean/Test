def step1_gas_required(row1, std, total_energy, total_emission, penalty, fuel_defaults_FEUM, green_fuel_type):
    lhv = row1["LHV"]
    gfi = row1["WtW"]
    inside = row1["Ïó≠ÎÇ¥"]
    outside = row1["Ïó≠Ïô∏"]

    lng_lhv = fuel_defaults_FEUM[green_fuel_type]["LHV"]
    lng_gfi = fuel_defaults_FEUM[green_fuel_type]["WtW"]

