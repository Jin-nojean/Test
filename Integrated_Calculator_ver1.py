    theo_lng = fossil_energy / lng_lhv * 2 if penalty > 0 else 0

    cumulative_energy = total_energy
    cumulative_emission = total_emission * 1_000_000

    added_energy = theo_lng * lng_lhv * 0.5
    added_emission = added_energy * lng_gfi

