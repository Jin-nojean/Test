    # Ïã§ÏßàÍ∞í Í≥ÑÏÇ∞
    # Í∏∞Ï°¥ ÎàÑÏ†Å ÏóêÎÑàÏßÄ/Î∞∞Ï∂úÎüâ
    cumulative_energy = total_energy
    cumulative_emission = total_emission * 1_000_000 \
        + final_b100_step1 * b100_lhv * b100_gfi \
    - final_b100_step1 * b100_lhv * 0.5 * gfi1

    # Step1 Î∞òÏòÅÍ∞í
