    # ÏµúÏ¢ÖÍ∞í: Ïù¥Î°†Í∞í vs Ïã§ÏßàÍ∞í Ï§ë ÏûëÏùÄ Í∞í
    final_b100_2 = min(theo_b100_2, actual_b100)
